int	ft_sqrt(int nb)
{
	int	res;

	if (nb > 0)
	{
		res = 1;
		while (res * res <= nb)
		{
			if (res * res == nb)
				return (res);
			else if (res >= 46341)
				return (0);
			res++;
		}
	}
	return (0);
}
